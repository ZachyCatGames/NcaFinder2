void NcaHeader::Print() const{
    char magicTerminated[5];
    std::memcpy(magicTerminated, magic, sizeof(magic));
    magicTerminated[4] = 0;

    std::printf("Magic: %s\n", magicTerminated);
    PrintKey("Signature 1", sig1, sizeof(sig1));
    PrintKey("Signature 2", sig2, sizeof(sig2));
    std::printf(
        "\nDistribution Type:  %d\n"
        "Content Type:       %d\n"
        "Key Generation:     0x%X\n"
        "Key Area Key Index: %d\n"
        "Content Size:       0x%lX\n"
        "Program ID:         0x%016lX\n\n",
        distType, contentType, this->GetKeyGeneration(),
        kakIdx, contentSize, programId
    );

    std::printf("FS Entries:\n");
    for (int i = 0; i < 4; i++) {
        std::printf(
            "\tSection %d:\n"
            "\t\tBegin: 0x%lX (block 0x%X)\n"
            "\t\tEnd:   0x%lX (block 0x%X)\n",
            i,
            (uint64_t(fsEntries[i].startBlock) * 0x200), fsEntries[i].startBlock,
            (uint64_t(fsEntries[i].endBlock) * 0x200),   fsEntries[i].endBlock
        );
    }
}

void NcaHeader::GetDecryptedAesCtrKey(void* pDst, bool isDev) const {
    AesEcbDecryptor dec(g_KeyAreaKeys[isDev][kakIdx][this->GetKeyGeneration()], 0x10);
    dec.Decrypt(pDst, keyArea[2], sizeof(keyArea[2]));
}

void FsHeader::GetIv(void* pDst, uint64_t offset) const {
    uint64_t upperIv = __builtin_bswap64(this->GetAesCtrUpperIv());
    uint64_t offsetBigEndian = __builtin_bswap64(offset / 0x10);
    std::memcpy(pDst, &upperIv, sizeof(upperIv));
    std::memcpy(static_cast<unsigned char*>(pDst) + 8, &offsetBigEndian, sizeof(offsetBigEndian));
}

void FsHeader::Print() const {
    std::printf(
        "\tVersion:        %d\n"
        "\tFS Type:        %s (%d)\n"
        "\tHash Type:      %s (%d)\n"
        "\tEncryptionType: %s (%d)\n"
        "\tGeneration:     %d\n"
        "\tSecure Value:   %d\n",
        version,
        this->GetFsTypeString(), std::to_underlying(fsType),
        this->GetHashTypeString(), std::to_underlying(hashType),
        this->GetEncryptionTypeString(), std::to_underlying(encryptionType),
        generation, secureValue
    );

    std::printf("\tHash Info:\n");
    switch (hashType) {
    case HashType::HierarchicalIntegrityHash:
        integrityMetaInfo.Print();
        break;
    default:
        break;
    }
}

void FsHeader::IntegrityMetaInfo::Print() const {
    char magicTerm[5] = {};
    std::memcpy(magicTerm, magic, sizeof(magic));

    /* Print initial info. */
    std::printf(
        "\t\tMagic:            %s\n"
        "\t\tVersion:          %d\n"
        "\t\tMaster Hash Size: 0x%x\n",
        magicTerm, version, masterHashSize
    );

    /* Print master hash. */
    PrintKey("Master Hash", &masterHash, sizeof(masterHash), 2);

    /* Print level info. */
    std::printf("\t\tNumber of layers: %d\n", infoLevelHash.maxLayers);
    for (int i = 0; i < NumberOfLevels; i++) {
        const InfoLevelHash::LevelInfo& info = infoLevelHash.levelInfo[i];
        std::printf(
            "\t\tLevel %d:\n"
            "\t\t\tOffset:     0x%lx\n"
            "\t\t\tSize:       0x%lx\n"
            "\t\t\tBlock Size: 0x%x (pow %d)\n",
            i, info.offset, info.size, 1u << info.blockOrder, info.blockOrder
        );
    }
}
